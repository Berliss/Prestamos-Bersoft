    private void prueba() {
        KeyboardFocusManager kb = KeyboardFocusManager.getCurrentKeyboardFocusManager();

        kb.addKeyEventPostProcessor(new KeyEventPostProcessor() {
            @Override
            public boolean postProcessKeyEvent(KeyEvent e) {

                if (e.getKeyCode() == KeyEvent.VK_F10 && e.getID() == KeyEvent.KEY_PRESSED && e.getModifiers() == 0) {
                    if (jTabbedPane1.getSelectedIndex() == 3) {
                        eliminarRegistrosEnTablita(tablaAtrasosARegistrar);
                        txtCuota.grabFocus();
                        return false;
                    }
                }

                if (e.getKeyCode() == KeyEvent.VK_F11 && e.getID() == KeyEvent.KEY_PRESSED && e.getModifiers() == 0) {
                    if (jTabbedPane1.getSelectedIndex() == 3) {
                        aplicarAtrasos();
                        return false;
                    }
                }

                return true;
            }
        });

    }

